package cn.south.toast.base.dto;

/**
 * 用户类型
 * @author Liang
 *
 */
public class UserTypeInDTO {
	private int num;
	private String code;
	private String msg;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
